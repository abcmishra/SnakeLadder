package model;

public class Ladder {
    int start = 0;
    int end = 0;

    public Ladder(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }


    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }




}